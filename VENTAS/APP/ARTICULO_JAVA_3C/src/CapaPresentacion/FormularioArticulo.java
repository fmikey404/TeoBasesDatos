    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int Res = JOptionPane.showConfirmDialog(null, 
                "¿Está Seguro de ELIMINAR el Articulo " + this.TxtFldCodigo.getText());
        if (Res == 0 && ValidarVacios()){
            DataArticulo objart = new DataArticulo();
            objart.setArt_cod(this.TxtFldCodigo.getText());
            JOptionPane.showMessageDialog(null, objart.EliminarArticulo());
            ListarArticulos();
            JOptionPane.showMessageDialog(null, "Articulo Eliminado");
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGrabarActionPerformed
        if(ValidarVacios()){
            DataArticulo objart = new DataArticulo();
            objart.setArt_cod(this.TxtFldCodigo.getText());
            objart.setArt_nom(this.TxtFldNombre.getText());
            objart.setArt_uni(this.TxtFldUni.getText());
            objart.setArt_pre(Double.parseDouble(this.TxtFldPrecio.getText()));
            objart.setArt_stk(Integer.parseInt(this.TxtFldCantidad.getText()));
            objart.setArt_marca(this.TxtFldMarca.getText());
            JOptionPane.showMessageDialog(null, objart.GrabarArticulo());
            ListarArticulos();
        }
    }//GEN-LAST:event_BtnGrabarActionPerformed

