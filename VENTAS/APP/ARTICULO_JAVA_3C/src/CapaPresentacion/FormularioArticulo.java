     public boolean ValidarVacios(){
         if(this.TxtFldCodigo.getText().equals("")){
             JOptionPane.showMessageDialog(null, "El código está vacío");
             return false;
         }else if (this.TxtFldNombre.getText().equals("")){
             JOptionPane.showMessageDialog(null, "El nombre está vacío");
             return false;
         }else if (this.TxtFldUni.getText().equals("")){
             JOptionPane.showMessageDialog(null, "La unidad de medida está vacía");
             return false;
         }else if (this.TxtFldPrecio.getText().equals("")){
             JOptionPane.showMessageDialog(null, "El precio está vacío");
             return false;
         }else if (this.TxtFldCantidad.getText().equals("")){
             JOptionPane.showMessageDialog(null, "La cantidad está vacía");
             return false;
         }else if (this.TxtFldMarca.getText().equals("")){
             JOptionPane.showMessageDialog(null, "La marca está vacía");
             return false;
         }else{
             return true;
         }
     }

